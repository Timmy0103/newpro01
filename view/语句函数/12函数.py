'''
函数是可重用的程序代码。
函数的作用，不仅可以实现代码的复用，更能实现代码的一致性。
一致性指的是，只要修改函数的代码，则所有调用该函数的地方都能得到体现。
'''

'''
函数的基本概念
1、一个程序由一个个任务组成，函数就是代表一个任务或者一个功能
2、函数是代码复用的通用机制
'''

'''
python函数的分类
1、内置函数：我们前面适用的str()、list()、len()等这些都是内置函数，我们可以拿来直接使用
2、标准库函数：我们可以通过import语句导入库，然后使用其中定义的函数
3、第三方库函数：python社区提供了很多高质量的库，下载安装这些库，通过import语句导入，就可以使用第三方库的函数
4、用户自定义函数：用户自己定义的函数，显然也是开发中适应用户自身需求定义的函数
'''